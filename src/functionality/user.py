class User:
    def __init__(self, name, salary, state, filing_status, post_tax_funds, post_tax_monthly_funds, budget, expenses):
        self.name = name
        self.salary = salary
        self.state = state
        self.filing_status = filing_status
        self.post_tax_funds = post_tax_funds
        self.post_tax_monthly_funds = post_tax_monthly_funds
        self.budget = budget
        self.expenses = expenses
